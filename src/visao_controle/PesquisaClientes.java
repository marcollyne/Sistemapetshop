    private void jtListaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtListaClientesMouseClicked
        if (evt.getClickCount() == 2) {
            int linhaSelecionada = jtListaClientes.getSelectedRow();
            Pessoa pessoaSelecionada = this.pessoasPesquisadas.get(linhaSelecionada);
            CadastroClienteAnimal telaCadastro = new CadastroClienteAnimal();
            telaCadastro.setPessoaEmEdicao(pessoaSelecionada);
            telaCadastro.setVisible(true);
            rodarPesquisa(); //Atuaizar a lista apÃ³s cadastro/alteraÃ§ao
        }
    }//GEN-LAST:event_jtListaClientesMouseClicked

