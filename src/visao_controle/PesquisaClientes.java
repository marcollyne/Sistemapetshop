    private void jtfFiltroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfFiltroKeyReleased
        if (jtfFiltro.getText().equals("")) {
            rodarPesquisa();
        } else if ((evt.getKeyChar() + "").matches("[0-9A-Za-zÃ£Ã¡Ã©Ã³Ãµ\\- ]")) {
            rodarPesquisa();
        }
    }//GEN-LAST:event_jtfFiltroKeyReleased

    private void jtfFiltroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfFiltroKeyTyped
        if (!(evt.getKeyChar() + "").matches("[0-9A-Za-zÃ£Ã¡Ã©Ã³Ãµ\\- ]")) {
            evt.consume();
        } else if (String.valueOf(evt.getKeyChar()).matches(",")) {
            evt.consume();
        } else {
            rodarPesquisa();
        }
    }//GEN-LAST:event_jtfFiltroKeyTyped

    public void rodarPesquisa() {
        PessoaAnimalDAO dao = new PessoaAnimalDAO();

        try {
            this.pessoasPesquisadas = dao.pesquisar(jtfFiltro.getText());
            DefaultTableModel modeloTabela = (DefaultTableModel) jtListaClientes.getModel();
            modeloTabela.setNumRows(0); //Limpa a tabela
            if (this.pessoasPesquisadas != null) {
                for (Pessoa p : this.pessoasPesquisadas) {
                    Object[] linha = new Object[2];
                    linha[0] = p.getNome();
                    linha[1] = p.getCidade().getNome();
                    modeloTabela.addRow(linha);
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao pesquisar no banco de dados");
        }
    }

