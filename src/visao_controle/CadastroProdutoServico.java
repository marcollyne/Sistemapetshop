    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        if (preecheuOsCampos()) {
            try {
                getDados();
                psd.salvar(psEdicao);
                JOptionPane.showMessageDialog(this, "Salvo com sucesso!", "Sucesso",
                        JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
            } catch (Exception ex) {
                Logger.getLogger(ProdutoServicoDAO.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Preencha todos os campos corretamente", "Erro",
                        JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

    private void jtfFrequenciaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfFrequenciaKeyTyped

        if (!String.valueOf(evt.getKeyChar()).matches("[0-9]")) {
            evt.consume();
        }
    }//GEN-LAST:event_jtfFrequenciaKeyTyped

    private void jtfValorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfValorKeyTyped
        if (!String.valueOf(evt.getKeyChar()).matches("[0-9 ,]")) {
            evt.consume();
        }
    }//GEN-LAST:event_jtfValorKeyTyped

    private void jtfNomeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfNomeKeyTyped
        //jtfNome.setForeground(Color.getHSBColor(150, 5, 17));
        if (!String.valueOf(evt.getKeyChar()).matches("[0-9, A-Z, a-z, ç, ã]")) {

            evt.consume();
        }
        if (String.valueOf(evt.getKeyChar()).matches(",")) {

            evt.consume();
        }

    }//GEN-LAST:event_jtfNomeKeyTyped

