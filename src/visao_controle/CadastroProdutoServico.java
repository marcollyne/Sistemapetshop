    private boolean preecheuOsCampos() {
        if (jtfNome.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "O campo nome não pode ser vazio", "Atenção",
                    JOptionPane.WARNING_MESSAGE);
            jtfNome.requestFocus();
            return false;
        } else if (jtfValor.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "O campo valor não pode ser vazio", "Atenção",
                    JOptionPane.WARNING_MESSAGE);
            jtfNome.requestFocus();
        }
        return true;
    }

    private void getDados() {
        psEdicao.setNome(jtfNome.getText());
        psEdicao.setValor(Float.parseFloat(jtfValor.getText().replaceAll(",", ".")));

        if (jtfFrequencia.getText().equals("")) {
            psEdicao.setFrequenciaAplicacao(Integer.parseInt(jtfFrequencia.getText().replaceAll("", "0")));
        } else {
            psEdicao.setFrequenciaAplicacao(Integer.parseInt(jtfFrequencia.getText()));
        }
        if (jrbServico.isSelected()) {
            psEdicao.setServico(true);
        } else {
            psEdicao.setServico(false);
        }
    }

    private void setDados() {
        jtfNome.setText(psEdicao.getNome());
        jtfValor.setText(String.valueOf(psEdicao.getValor()));
        jtfFrequencia.setText(String.valueOf(psEdicao.getFrequenciaAplicacao()));

        if (psEdicao.isServico() == true) {
            jrbServico.doClick();
        } else {
            jrbProduto.doClick();
        }
        jlAlteraTexto.setText("Alterar dados de");
    }

